package org.eclipse.che.examples;

public class Calculator {
    
    public static int addInt(int x, int y){

        int z = x + y;

        return z;
    }

    public static double addDouble(double x, double y){

        double z = x + y;

        return z;
    }

    public static long addLong(long x, long y){

        long z = x + y;

        return z;
    }

}